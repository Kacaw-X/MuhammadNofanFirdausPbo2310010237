    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(Nik.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Id belum diisi");
            Nik.requestFocus();
        }else if (Nama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi id kelompok terlebih dahulu");
            Nama.requestFocus();
        }else if (JenisKelamin.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi pemilik terlebih dahulu");
            JenisKelamin.requestFocus();
        }
        else if (Agama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi noSK terlebih dahulu");
            Agama.requestFocus();
        }
        else if (TanggalLahir.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi noSK terlebih dahulu");
            TanggalLahir.requestFocus();
        }

        else {
            tersangka.simpanTersangkaPRE(
                Nik.getText(),
                Nama.getText(),
                JenisKelamin.getText(),
                Agama.getText(),
                TanggalLahir.getText(),
                ID.getText()
            );

            if (tersangka.validasi == true) {
                Nik.setText(tersangka.VARNIK);
                Nama.setText(tersangka.VARNAMA);
                JenisKelamin.setText(tersangka.VARJENISKELAMIN);
                Agama.setText(tersangka.VARAGAMA);
                TanggalLahir.setText(tersangka.VARTANGGALLAHIR);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

