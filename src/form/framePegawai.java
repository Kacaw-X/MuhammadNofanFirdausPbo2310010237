    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(id.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Id belum diisi");
            id.requestFocus();
        }else if (nama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi id kelompok terlebih dahulu");
            nama.requestFocus();
        }else if (jabatan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi pemilik terlebih dahulu");
            jabatan.requestFocus();
        }
        else if (nip.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi noSK terlebih dahulu");
            nip.requestFocus();
        }
        else if (nrp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi noSK terlebih dahulu");
            nrp.requestFocus();
        }

        else {
            pegawai.simpanPegawaiPRE(
                id.getText(),
                nama.getText(),
                jabatan.getText(),
                nip.getText(),
                nrp.getText()
            );

            if (pegawai.validasi == true) {
                nama.setText(pegawai.VARNAMA);
                jabatan.setText(pegawai.VARJABATAN);
                nip.setText(pegawai.VARNIP);
                nrp.setText(pegawai.VARNRP);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

